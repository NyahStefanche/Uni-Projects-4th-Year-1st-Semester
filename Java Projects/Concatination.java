public class Concatination
{
	public static void main(String[] args)
	{
		String first="Boris";
		String last="Ivanov";
		System.out.println(first+last);
		System.out.println(first+" "+last);
		System.out.println(first+", "+last);
		String name=first+" "+last;
		System.out.println(name);
	}
}